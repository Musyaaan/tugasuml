    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
try {            // 1. Mengambil data dari TextField dan mengubahnya jadi angka
            double luasTersedia = Double.parseDouble(txtLuasTersedia.getText());
            double hargaTanahPerM2 = Double.parseDouble(txtHargaTanah.getText());
            double hargaBangunan = Double.parseDouble(txtHargaBangunan.getText());
            double dp = Double.parseDouble(txtDP.getText());
            int lamaCicilan = Integer.parseInt(txtCicilan.getText()); // txtCicilan adalah lama cicilan

            // 2. Rumus Hitungan
            double hargaTotal = (luasTersedia * hargaTanahPerM2) + hargaBangunan;
            double ppn = 0.10 * hargaTotal;
            double cicilanBulan = (hargaTotal + ppn - dp) / lamaCicilan;

            // 3. Menampilkan hasil perhitungan kembali ke Form
            txtHarga.setText(String.valueOf(hargaTotal));
            txtPPN.setText(String.valueOf(ppn));
            txtCicilanBulan.setText(String.valueOf(cicilanBulan)); // Ini yang benar, bukan txtCicilan

            // 4. Memunculkan kotak konfirmasi
            javax.swing.JOptionPane.showMessageDialog(this, "PELANGGAN KONFIRM/SETUJU");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pastikan semua data angka telah diisi dengan benar!", "Error Input", javax.swing.JOptionPane.ERROR_MESSAGE);
        }   // TODO add your handling code here:
    }//GEN-LAST:event_btnProsesActionPerformed

