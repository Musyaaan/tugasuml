    private void DecisiontreeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DecisiontreeActionPerformed
   try {
        // ambil data input
        double luasTersedia = Double.parseDouble(txtLuasTersedia.getText());
        double hargaTanahPerM2 = Double.parseDouble(txtHargaTanah.getText());
        double hargaBangunan = Double.parseDouble(txtHargaBangunan.getText());
        double dp = Double.parseDouble(txtDP.getText());
        int lamaCicilan = Integer.parseInt(txtCicilan.getText());


        // hitung total
        double hargaTotal = (luasTersedia * hargaTanahPerM2)
                            + hargaBangunan;
        double ppn = 0.10 * hargaTotal;
        double totalDenganPPN = hargaTotal + ppn;
        double cicilanBulan =
                (totalDenganPPN - dp) / lamaCicilan;

        // TAMPILKAN HASIL HITUNG
        txtHarga.setText(String.valueOf(hargaTotal));
        txtPPN.setText(String.valueOf(ppn));
        txtCicilanBulan.setText(String.valueOf(cicilanBulan));

        // decision tree C4.5
        double dpPersen =
                (dp / totalDenganPPN) * 100;
        String hasil;
        /*
            Aturan decision
            IF DP >= 20%
            AND Cicilan <= 120 bulan
            THEN disetujui
            ELSE ditolak
        */
        if (dpPersen >= 20 && lamaCicilan <= 120) {
            hasil = "DISETUJUI";
        } else {
            hasil = "DITOLAK";
        }
        // tampilkan hasil

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "HASIL DECISION TREE C4.5\n\n"
                + "DP (%) : " + String.format("%.2f", dpPersen) + "%\n"
                + "Lama Cicilan : " + lamaCicilan + " bulan\n\n"
                + "STATUS : " + hasil
        );

    } catch (NumberFormatException e) {

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Pastikan semua data angka diisi!",
                "ERROR",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_DecisiontreeActionPerformed

