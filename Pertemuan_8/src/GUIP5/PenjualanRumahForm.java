    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {
    // 1. Memanggil koneksi yang sudah kita buat di TestKoneksi / class Koneksi
    // Pastikan memanggil nama class koneksi yang benar, misalnya pertemuan_8.TestKoneksi.getKoneksi() 
    // jika Anda menambahkan method getKoneksi() di sana. Jika Anda buat class terpisah bernama Koneksi, gunakan Koneksi.getKoneksi()
    java.sql.Connection cn = koneksi.getKoneksi(); 
    
    // 2. Perintah SQL INSERT (11 tanda tanya karena ada 11 kolom di tabel)
    String sql = "INSERT INTO jual_rumah (nama_pemesan, area, harga_tanah, harga_bangunan, tipe_rumah, luas_tanah, harga_total, dp, lama_cicilan, ppn, cicilan_perbulan) VALUES (?,?,?,?,?,?,?,?,?,?,?)";
    
    java.sql.PreparedStatement pStat = cn.prepareStatement(sql);
    
    // 3. Menentukan Tipe Rumah dari RadioButton yang dipilih
    String tipe = "";
    if (rbTipe36.isSelected()) {
        tipe = "Tipe-36";
    } else if (rbTipe49.isSelected()) {
        tipe = "Tipe-49";
    } else if (rbTipe90.isSelected()) {
        tipe = "Tipe-90";
    }

    // 4. Mengisi nilai parameter (?,?,?) dengan data dari Form
    pStat.setString(1, txtNama.getText());
    pStat.setString(2, cbArea.getSelectedItem().toString());
    pStat.setDouble(3, Double.parseDouble(txtHargaTanah.getText()));
    pStat.setDouble(4, Double.parseDouble(txtHargaBangunan.getText()));
    pStat.setString(5, tipe);
    pStat.setInt(6, Integer.parseInt(txtLuasAsli.getText()));
    pStat.setDouble(7, Double.parseDouble(txtHarga.getText()));
    pStat.setDouble(8, Double.parseDouble(txtDP.getText()));
    pStat.setInt(9, Integer.parseInt(txtCicilan.getText()));
    pStat.setDouble(10, Double.parseDouble(txtPPN.getText()));
    pStat.setDouble(11, Double.parseDouble(txtCicilanBulan.getText()));
    
    // 5. Eksekusi penyimpanan ke MySQL
    pStat.executeUpdate();
    
    javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan ke Database MySQL!");
    
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

